package com.fitness.activityservice.model;

/**
 * Enum representing the types of activities a user can perform.
 */
public enum ActivityType {
    RUNNING,      // Running activity
    WALKING,      // Walking activity
    CYCLING,      // Cycling activity
    SWIMMING,     // Swimming activity
    WEIGHT_TRAINING, // Weight training activity
    YOGA,         // Yoga activity
    HIIT,         // High-intensity interval training
    CARDIO,       // Cardio activity
    STRECHING,    // Stretching activity
    OTHER         // Other activity types
}
